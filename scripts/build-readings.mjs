import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import path from "node:path";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const approval = JSON.parse(await readFile(path.join(root, "content", "approved-readings.json"), "utf8"));
const academyImport = JSON.parse(await readFile(path.join(root, "content", "imports", "bal-e-jibril-first-seven.json"), "utf8"));
const importedById = new Map(academyImport.readings.map(reading => [reading.id, reading]));

if (approval?.schemaVersion !== 1 || !Array.isArray(approval.ids) || !approval.ids.length) {
  throw new Error("approved-readings.json must contain a non-empty schemaVersion 1 ids array.");
}

const readings = [];
for (const id of approval.ids) {
  const draftPath = path.join(root, "content", "drafts", `${id}.json`);
  const draft = JSON.parse(await readFile(draftPath, "utf8"));
  if (draft.id !== id || !draft.source?.sourceChecked) throw new Error(`Unverified or mismatched draft: ${id}`);
  if (draft.annotations.verseAnnotations.length !== draft.source.verses.length) throw new Error(`Incomplete draft: ${id}`);

  readings.push({
    id,
    collection: draft.source.collection,
    title: draft.source.title,
    transliteration: draft.source.transliteration || importedById.get(id)?.transliteration || id,
    sourceUrl: draft.source.sourceUrl,
    historicalContext: normalizeHistoricalContext(draft),
    reflection: draft.annotations.reflectionQuestion,
    takeaway: draft.annotations.takeaway,
    verses: draft.source.verses.map((verse, index) => {
      const annotation = draft.annotations.verseAnnotations[index];
      return {
        lines: verse.lines,
        explanation: annotation.explanationUrdu,
        englishTranslation: annotation.englishTranslation || "",
        words: annotation.difficultWords.map(word => [word.term, word.meaningUrdu])
      };
    })
  });
}

const browserReadings = readings.map(reading => ({
  ...reading,
  verses: reading.verses.map(verse => ({
    urdu: verse.lines,
    meaning: verse.explanation,
    english: verse.englishTranslation,
    words: verse.words.map(([term, meaning]) => ({ term, meaning }))
  }))
}));

await writeFile(
  path.join(root, "data.js"),
  `// Generated by npm run build:readings. Do not edit by hand.\nwindow.READINGS = ${JSON.stringify(browserReadings, null, 2)};\n`,
  "utf8"
);
await writeFile(
  path.join(root, "gmail-delivery", "Readings.gs"),
  `// Generated by npm run build:readings. Paste this file into the Apps Script project.\nconst GENERATED_READINGS = ${JSON.stringify(readings, null, 2)};\n`,
  "utf8"
);

console.log(`Published ${readings.length} approved readings to the browser and Gmail libraries.`);

function normalizeHistoricalContext(draft) {
  const context = draft.annotations.historicalContext;
  const collectionContext = collectionLevelHistoricalContext(draft);
  if (collectionContext) return collectionContext;

  if (context?.summaryUrdu?.trim() || context?.summaryEnglish?.trim()) {
    return {
      period: context.period || "Unknown",
      place: context.place || "Unknown",
      summaryUrdu: context.summaryUrdu || draft.annotations.historicalContextUrdu || "",
      summaryEnglish: context.summaryEnglish || "",
      confidence: context.confidence || "unknown",
      sourceNote: context.sourceNote || "Context requires human review."
    };
  }

  return {
    period: "Unknown",
    place: "Unknown",
    summaryUrdu: draft.annotations.historicalContextUrdu || "اس نظم کے خاص زمان و مکان کے لیے مستند ماخذ درکار ہے۔",
    summaryEnglish: "The exact writing date and place for this poem are not verified in the current source metadata.",
    confidence: "unknown",
    sourceNote: "Poem-specific date and place are not verified in the local metadata."
  };
}

function collectionLevelHistoricalContext(draft) {
  const collection = draft.source?.collection || "";
  if (!collection.includes("بالِ جبریل")) return null;

  return {
    period: "Published in Bal-e-Jibril, 1935",
    place: "Unknown",
    summaryUrdu: "یہ نظم بالِ جبریل میں شامل ہے، جو ۱۹۳۵ میں شائع ہوا۔ اس نظم کا خاص زمانہ اور مقامِ تحریر ابھی مقامی ماخذ میں verified نہیں، اس لیے اسے مجموعے کے عمومی تاریخی پس منظر کے طور پر پڑھیں، نہ کہ poem-specific دعوے کے طور پر۔",
    summaryEnglish: "This poem appears in Bal-e-Jibril, published in 1935. The exact writing date and place for this specific poem are not verified in the local source metadata, so this is collection-level context rather than a poem-specific claim.",
    confidence: "likely",
    sourceNote: "Collection-level publication context only; exact poem-specific writing date and place are unknown."
  };
}
